class Solution:
    def judgePoint24(self, nums):
        if len(nums)==1: return isclose(nums[0], 24)
        return any(self.judgePoint24([x]+rest)
                for a, b, *rest in permutations(nums)
                for x in {a+b, a-b, a*b, b and a/b})



