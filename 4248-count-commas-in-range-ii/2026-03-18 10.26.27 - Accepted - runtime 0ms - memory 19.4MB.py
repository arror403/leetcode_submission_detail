class Solution:
    def countCommas(self, n: int) -> int:
        # 1,000,000,000,000,000 return 3998998998999005
        #                                               previous
        #     1,000,000,000,000 ~ 999,999,999,999,999   (999000000000)*3
        #         1,000,000,000 ~     999,999,999,999   (999000000)*2
        #             1,000,000 ~         999,999,999   999000
        #                 1,000 ~             999,999   0
        L=len(str(n))

        if L<4: return 0
        if L<7: return n-999
        if L<10: return (n-999999)*2+999000
        if L<13: return (n-999999999)*3+(999000000)*2+999000
        if L<16: return (n-999999999999)*4+(999000000000)*3+(999000000)*2+999000

        return 3998998998999005